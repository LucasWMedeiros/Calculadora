        public void calculo(){
    switch(calculation){
        case 1: {
            resp = num + Double.parseDouble(jtfTela.getText());
            jtfTela.setText(Double.toString(resp));
            break;
        }
        case 2:{
            resp = num  - Double.parseDouble(jtfTela.getText());
            jtfTela.setText(Double.toString(resp));
            break;
        }
        case 3: {
            resp = num * Double.parseDouble(jtfTela.getText());
            jtfTela.setText(Double.toString(resp));
            break;
        }
        case 4: {
            resp = num / Double.parseDouble(jtfTela.getText());
            jtfTela.setText(Double.toString(resp));
            break;
        }
    }
}
        public void calculoporcentagem(){
            switch(calculation){
        case 1: {
            resp = num + (num*porcentagem);
            jtfTela.setText(Double.toString(resp));
            break;
        }
        case 2:{
            resp = num - (num*porcentagem) ;
            jtfTela.setText(Double.toString(resp));
            break;
        }
        case 3: {
            resp = num * porcentagem ;
            jtfTela.setText(Double.toString(resp));
            break;
        }
        case 4: {
            resp = num / (num*porcentagem);
            jtfTela.setText(Double.toString(resp));
            break;
        }
    }
        }
        
